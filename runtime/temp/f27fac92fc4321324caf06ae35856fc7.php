	<script type="text/javascript">
	function verify(){
		$('#captchaImg').attr('src','/index.php?m=Home&c=User&a=verify&r='+Math.random());
	}

	var uflag = false;
	var cflag = false;

	function chkuser(){
		var username = $('#username').val();
		if(username == ''){
			show_error('username','用户名不能为空');
			return false;
		}else{
			$.ajax({
				url:'/index.php?m=Home&c=User&a=check_username&t='+Math.random(),
				type:'post',
				dataType:'json',
				data:{username:username},
				success:function(t){
					if(t>=1){
						uflag = true;
					}else{
						show_error('username','用户名错误');
					}
				}
			})
		}
	}

	function chkcaptcha(){
		var captcha = $('#captcha').val();
		if(captcha == ''){
			show_error('captcha','验证码不能为空');
			return false;
		}else if(captcha.length!=4){
			show_error('captcha','验证码错误');
			return false;
		}else{
			$.ajax({
				url:'/index.php?m=Home&c=User&a=check_captcha&t='+Math.random(),
				type:'post',
				dataType:'json',
				data:{verify_code:captcha},
				success:function(t){
					if(t==1){
						cflag = true;
						gonext();
					}else{
						show_error('captcha','验证码不正确');
						return false;
					}
				}
			})
		}
	}

	function gonext(){
		if($('#username').val() == ''){
			show_error('username','用户名不能为空');
			return;
		}
		if($('#captcha').val() == ''){
			show_error('captcha','验证码不能为空');
			return;
		}
		if(!uflag){
			show_error('username','用户名验证有误');
			return;
		}else if(!cflag){
			show_error('captcha','图形验证码有误');
			return;
		}
		if(uflag && cflag){
			$('#forget_pwd').submit();
		}
	}

	function show_error(type,msg){
        verify()
		layer.alert(msg, {icon: 2});
		return false;
	}
